package com.whitehole.socialmap.network.server;

import java.util.ArrayList;

public class CCoolSPicture {
	public ArrayList<CoolSPicture> data;
	
	public CCoolSPicture(CoolSPicture data){
		this.data.add(data);
	}
	
	public CCoolSPicture(ArrayList<CoolSPicture> data){
		this.data = data;
	}
	
	public ArrayList<CoolSPicture> getData(){
		return data;
	}
	
	public void setData(ArrayList<CoolSPicture> data){
		this.data = data;
	}

	@Override
	public String toString() {
		return "CCoolSPicture [data=" + data.size()	+ "]";
	}
	
	
}
